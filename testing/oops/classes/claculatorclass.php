<?php 
class calculator{
    public function  add($input1,$input2)
    {   
        return $input1 + $input2;
    }
    public function  subtract($input1,$input2)
    {   
        return $input1 - $input2;
    }
    public function  multiple($input1,$input2)
    {   
        return $input1 * $input2;
        
    }
    public function  divide($input1,$input2)
    {   
        return $input1 / $input2;
    }
}
?>